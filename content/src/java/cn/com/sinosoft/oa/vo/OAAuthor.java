
package cn.com.sinosoft.oa.vo;


public class OAAuthor{
    protected String gradeId;
    protected String productions;
    protected String permitComs;
    protected String exceComs;
	public String getGradeId() {
		return gradeId;
	}
	public void setGradeId(String gradeId) {
		this.gradeId = gradeId;
	}
	public String getProductions() {
		return productions;
	}
	public void setProductions(String productions) {
		this.productions = productions;
	}
	public String getPermitComs() {
		return permitComs;
	}
	public void setPermitComs(String permitComs) {
		this.permitComs = permitComs;
	}
	public String getExceComs() {
		return exceComs;
	}
	public void setExceComs(String exceComs) {
		this.exceComs = exceComs;
	} 
}