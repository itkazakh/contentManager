package cn.com.sinosoft.saa.vo;

public class SaaRiskObjectVO {
	private String riskObjecCode;
	private String riskObjectName;
	private String riskObjectParentCode;
	private String value;
	private String hasPower;
	public String getRiskObjecCode() {
		return riskObjecCode;
	}
	public void setRiskObjecCode(String riskObjecCode) {
		this.riskObjecCode = riskObjecCode;
	}
	public String getRiskObjectName() {
		return riskObjectName;
	}
	public void setRiskObjectName(String riskObjectName) {
		this.riskObjectName = riskObjectName;
	}
	public String getRiskObjectParentCode() {
		return riskObjectParentCode;
	}
	public void setRiskObjectParentCode(String riskObjectParentCode) {
		this.riskObjectParentCode = riskObjectParentCode;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getHasPower() {
		return hasPower;
	}
	public void setHasPower(String hasPower) {
		this.hasPower = hasPower;
	}
}
